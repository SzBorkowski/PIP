package PGO_12c.cw1;

public class zd4 {
    public static void main(String[] args) {
        int counter = 0;
        while(counter < 10)
        {
            System.out.println("Hello world!" + (counter+1));
            counter++;
        }

        for (int i = 0; i < 10; i++){
            System.out.println("Hello world!" + (i+1));
        }
        System.out.println();
    }
}
